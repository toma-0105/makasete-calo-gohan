# meal_ingredients: 料理とアレルギー食材の紐づけ
# 方針: 料理名から明確に分かる主原料（肉・魚・乳製品・卵料理・麦製品など）のみを対象とする。
#       唐揚げの衣の小麦粉、ハンバーグのつなぎの卵、炒め物のバター醤油など、
#       ソースや衣に少量使われる「隠れた」原材料は対象外とする。
# ※ 同名の料理（例: 朝食・昼夕で別レコードの「ご飯」）は同じ原材料なので、name一致で一括紐づけする
def link_meal_allergens(mapping)
  mapping.each do |meal_name, allergen_names|
    meals = MealMaster.where(name: meal_name)
    allergen_names.each do |allergen_name|
      allergen = AllergenMaster.find_by!(name: allergen_name)
      meals.each do |meal|
        MealIngredient.create!(meal_master: meal, allergen_master: allergen)
      end
    end
  end
end

